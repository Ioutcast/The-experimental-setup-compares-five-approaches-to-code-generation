package ru.master.exp.baseline;

/**
 * Статусы жизненного цикла заявки в ручном эталоне.
 *
 * Набор статусов соответствует workflow, используемому в эксперименте:
 * от черновика до закрытия, отклонения, отмены или истечения срока обработки.
 */
public enum RequestStatus {
    /** Черновик заявки. */
    DRAFT,
    /** Заявка отправлена автором. */
    SUBMITTED,
    /** Для заявки определяется маршрут согласования. */
    ROUTING,
    /** Заявка находится на согласовании. */
    IN_APPROVAL,
    /** Заявка возвращена автору на доработку. */
    REVISION_REQUIRED,
    /** Заявка отклонена. */
    REJECTED,
    /** Заявка согласована. */
    APPROVED,
    /** Заявка ожидает исполнения во внешней системе. */
    EXECUTION_PENDING,
    /** Исполнение заявки подтверждено. */
    EXECUTED,
    /** Заявка закрыта. */
    CLOSED,
    /** Срок обработки заявки истек. */
    EXPIRED,
    /** Заявка отменена. */
    CANCELLED
}
