package ru.master.exp.baseline;

/**
 * Роли пользователей, участвующие в обработке и согласовании заявок.
 */
public enum Role {
    /** Автор заявки. */
    AUTHOR,
    /** Руководитель подразделения. */
    DEPARTMENT_MANAGER,
    /** Сотрудник финансового отдела. */
    FINANCE_OFFICER,
    /** Сотрудник службы безопасности. */
    SECURITY_OFFICER,
    /** Сотрудник юридического отдела. */
    LEGAL_OFFICER,
    /** Директор или руководитель более высокого уровня. */
    DIRECTOR,
    /** Администратор системы. */
    ADMIN
}
